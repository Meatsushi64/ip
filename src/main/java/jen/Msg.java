package jen;
/**
 * Provides predefined messages used by the chatbot.
 * This class contains constants for commonly used chatbot messages.
 */
public class Msg {
    /** Line separator for chatbot messages. */
    public static final String LINE = "_______________________";
    /** Greeting message displayed when the chatbot starts. */
    public static final String GREETING = "Hello! I'm Jen. \n"
            + "What can I do for you?";
    /** Goodbye message displayed when the chatbot exits. */
    public static final String GOODBYE = "Bye. Hope to see you again soon!\n";
}
